;HEX TO BCD in 8051

START:
MOV R1, #0FFH	;INITIALIZE HEX VALUE

MOV A, R1
MOV B, #64H
DIV AB			;DIVIDE BY 100
MOV R2, A		;R2 HAS THE MSB
MOV A, B		
MOV B, #0AH	
DIV AB			;DIVIDE BY 10
MOV R3, A		;R3 HAS 2ND MSB
MOV R4, B		;R4 HAS LSB

MOV A, R2
MOV B, #10H
MUL AB			;MSB * 10
ADD A, R3		;(MSB * 10) + 2ND MSB
MOV B, #10H
MUL AB			;(MSB * 100 + 2ND MSB * 10)
MOV R5, B		;HIGHER BYTE
MOV R6, A		;LOWER BYTE
ADD A, R4		; + LSB 
MOV R6, A		;MOV RESULT TO A

HALT:
SJMP HALT